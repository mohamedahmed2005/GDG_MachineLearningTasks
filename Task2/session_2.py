"""### Challenge 3: Word Frequency Counter

Write a program that:
1. Takes a sentence as input
2. Counts how many times each word appears
3. Stores results in a dictionary
4. Prints words and their frequencies, sorted by frequency (highest first)

**Hint:**
- Use `.lower()` to make it case-insensitive
- Use `.split()` to separate words
- You may need to remove punctuation

"""

